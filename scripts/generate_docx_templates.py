"""
Генерация стартовых .docx-шаблонов для сервиса юридических адресов.

Запустить один раз:
    pip install -r requirements.txt
    python generate_docx_templates.py

В папке ../templates/ создаст три файла:
    - template_contract.docx           (для смены адреса)
    - template_guarantee_initial.docx  (для первичной регистрации)
    - template_guarantee_full.docx     (для смены адреса)

После генерации редактирование производится прямо в Word/LibreOffice.
Все плейсхолдеры — стандартный синтаксис docxtpl (Jinja2).

Принцип отбора переменных: переменная только тогда, когда значение
действительно меняется от документа к документу. Бизнес-константы
(сроки, неустойки, формулы суда и т.п.) лежат в шаблоне обычным
текстом — юрист правит их в Word напрямую без участия разработчика.
"""
from pathlib import Path

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Cm, Pt

OUT_DIR = Path(__file__).resolve().parent.parent / "templates"


# ============================================================
# Helpers
# ============================================================

def new_doc():
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(12)
    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(1.5)
    return doc


def heading(doc, text, *, center=True, bold=True, size=12):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.LEFT
    r = p.add_run(text)
    r.bold = bold
    r.font.size = Pt(size)
    return p


def para(doc, text, *, justify=True, indent=True, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY if justify else WD_ALIGN_PARAGRAPH.LEFT
    if indent:
        p.paragraph_format.first_line_indent = Cm(1.25)
    r = p.add_run(text)
    r.bold = bold
    return p


def empty(doc):
    doc.add_paragraph()


def right(doc, lines):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    for i, line in enumerate(lines):
        if i > 0:
            p.add_run("\n")
        p.add_run(line)
    return p


